#include <stdlib.h>

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	l;
	size_t	i;

	l = 0;
	i = 0;
	while (src[l])
		l++;
	if (size > 0)
	{
		while (i < l && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (l);
}
