#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*newnode;

	newnode = malloc(sizeof(t_list));
	if (!newnode)
		return (NULL);
	if (!content)
		newnode->content = NULL;
	else
		newnode->content = content;
	newnode->next = NULL;
	return (newnode);
}
